package controlflowstatements;

public class IfElse {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        if (a + b > 20) {
            System.out.println("Value is greater than 20");
        } else {
            System.out.println("Value is less than 20");
        }
    }
}

